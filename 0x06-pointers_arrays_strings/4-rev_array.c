/**
 * reverse_array - reverse the content of an array
 * @a: array to reverse
 * @n: number of elements in array
 * Return: is void
 */

void reverse_array(int *a, int n)
{
	int i;

	i = 0;
	while (i < n)
	{
		a[i] = a[n - i - 1];
		i++;
	}

	i = 0;
	while (i < n)
	{
		a[i] = a[i];
		i++;
	}
}
